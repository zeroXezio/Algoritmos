numhojas = int(input("¿Cual es el numero de hojas del cuaderno: "))

if numhojas == 250:
    print ("El costo del cuaderno es de $16.000")
elif numhojas == 100:
    print ("El costo del cuaderno es de $11.000")
elif numhojas == 80:
    print ("El costo del cuaderno es de $8.000")
elif numhojas == 50:
    print ("El costo del cuaderno es de $4.500")
else:
    print ("Numero de hojas incorrecto")