import math

num = int(input("Ingresa el numero: "))

cuadrado = num * num
cubo = num * num * num

print(f'El cuadrado del numero es: {cuadrado}')
print(f'El cubo del numero es: {cubo}')
print(f'La raiz cuadrada del numero es: {math.sqrt(num)}')
print(f'La raiz cubica del numero es: {math.cbrt(num)}')
